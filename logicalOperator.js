// OR  => ||  states that if any operand is true it will return true
// AND=> && states that if two operand are true it will return true
//  NOT=> !  it states that if expression is true, NOT makes it false, if it is false NOT makes it true.

console.log("OR Table")
console.log("TorT",  true || true)
console.log("Tor F",  true || false)
console.log("For T",  false || true)
console.log("For F",  false || false)

console.log("&& Table")
console.log("T AND T", true && true )
console.log("T AND F", true && false )
console.log("F AND T", false && true )
console.log("F AND F", false && false )


